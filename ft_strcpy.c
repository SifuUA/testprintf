#include "ft_printf.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*arr;

	i = 0;
	arr = dst;
	while (src[i] != '\0')
	{
		arr[i] = src[i];
		i++;
	}
	arr[i] = '\0';
	return (dst);
}
